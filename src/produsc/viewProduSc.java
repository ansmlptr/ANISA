    private void TjadwalprodMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TjadwalprodMouseClicked
    int row = Tjadwalprod.getSelectedRow();
    if (row >= 0) {
        TFjadwal.setText(Tjadwalprod.getValueAt(row, 0).toString());
        TFproduk.setText(Tjadwalprod.getValueAt(row, 1).toString());
               
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date tglMulai = sdf.parse(Tjadwalprod.getValueAt(row, 2).toString());
            Date tglSelesai = sdf.parse(Tjadwalprod.getValueAt(row, 3).toString());
            
            DCmulai.setDate(tglMulai);
            DCselesai.setDate(tglSelesai);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, 
                "Error parsing tanggal: " + e.getMessage());
        }
        
        TFtotal.setText(Tjadwalprod.getValueAt(row, 4).toString());
        CBstatus.setSelectedItem(Tjadwalprod.getValueAt(row, 5).toString());
        TAcatatan.setText(Tjadwalprod.getValueAt(row, 6).toString());
        TFdibuat.setText(Tjadwalprod.getValueAt(row, 7).toString());
    }
    }//GEN-LAST:event_TjadwalprodMouseClicked

