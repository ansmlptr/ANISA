    private void BtambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtambahActionPerformed
        try {
        String kodeProduk = TFproduk.getText();
        
        java.sql.Date tglMulai = new java.sql.Date(DCmulai.getDate().getTime());
        java.sql.Date tglSelesai = new java.sql.Date(DCselesai.getDate().getTime());
        
        int totalProduksi = Integer.parseInt(TFtotal.getText());
        String status = CBstatus.getSelectedItem().toString().toLowerCase();
        String catatan = TAcatatan.getText();
        String dibuatOleh = TFdibuat.getText();

       controller.addData(kodeProduk, tglMulai, tglSelesai, totalProduksi, status, catatan, dibuatOleh);

       clearFields();
       refreshTable();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Input data tidak valid: " + e.getMessage());
    }
    }//GEN-LAST:event_BtambahActionPerformed

    private void BhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BhapusActionPerformed
        int idJadwal = Integer.parseInt(TFjadwal.getText());
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Apakah Anda yakin ingin menghapus data ini?", 
            "Konfirmasi Hapus", 
            JOptionPane.YES_NO_OPTION);
            
        if (confirm == JOptionPane.YES_OPTION) {
            controller.deleteData(idJadwal);
            clearFields();
            refreshTable();
        }
    }//GEN-LAST:event_BhapusActionPerformed

    private void BeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BeditActionPerformed
        int idJadwal = Integer.parseInt(TFjadwal.getText());
        String kodeProduk = TFproduk.getText();
        java.sql.Date tglMulai = new java.sql.Date(DCmulai.getDate().getTime());
        java.sql.Date tglSelesai = new java.sql.Date(DCselesai.getDate().getTime());        
        int totalProduksi = Integer.parseInt(TFtotal.getText());
        String status = CBstatus.getSelectedItem().toString().toLowerCase();
        String catatan = TAcatatan.getText();
        String dibuatOleh = TFdibuat.getText();

        controller.updateData(idJadwal, kodeProduk, tglMulai, tglSelesai, totalProduksi, status, catatan, dibuatOleh);
        clearFields();
        refreshTable();
    }//GEN-LAST:event_BeditActionPerformed

